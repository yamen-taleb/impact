import {z} from "zod";

export const personalInfoSchema = z.object({
    firstName: z.string()
        .min(2, "يجب أن يكون الاسم الأول على الأقل حرفين")
        .max(50, "يجب أن يكون الاسم الأول أقل من 50 حرفًا")
        .regex(/^[\u0600-\u06FFa-zA-Z\s]+$/, "يمكن أن يحتوي الاسم الأول على الحروف والمسافات فقط"),
    lastName: z.string()
        .min(2, "يجب أن يكون الكنية على الأقل حرفين")
        .max(50, "يجب أن تكون الكنية أقل من 50 حرفًا")
        .regex(/^[\u0600-\u06FFa-zA-Z\s]+$/, "يمكن أن تحتوي الكنية على الحروف والمسافات فقط"),
    phoneNumber: z.string()
        .min(10, "يجب أن يكون رقم الهاتف على الأقل 10 أرقام")
        .max(15, "يجب أن يكون رقم الهاتف أقل من 15 رقمًا")
        .regex(/^[\d\s\-\+\(\)]+$/, "يمكن أن يحتوي رقم الهاتف على الأرقام ورموز الهاتف فقط"),
});
